				<h4>SCI</h4>
				<p>SCI delivers treatment against the neglected tropical diseases (NTDs) schistosomiasis (bilharzia) and soil-transmitted helminths (worms), that affect over 500 million of the poorest people on earth.</p>
				<p>If left untreated these diseases cause stunted growth, disfigurement, chronic disability and an early death. They prevent children from going to school and adults from earning a living, trapping these communities in perpetual poverty.</p>
				<p>SCI is ranked one of the most cost-effective charities by two independent charity evaluators, <a href="http://www.givewell.org">www.givewell.org</a> and <a href="http://www.givingwhatwecan.org">www.givingwhatwecan.org</a>.</p>
